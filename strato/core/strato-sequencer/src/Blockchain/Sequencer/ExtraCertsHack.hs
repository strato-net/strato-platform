getCert :: [String] -> X509Certificate
getCert certLines =
  let eCert = 
        bsToCert $
          BC.pack $
            unlines certLines
   in case eCert of
        Left _ -> error $ "malformed cert passed to getCert: " <> unlines certLines
        Right c -> c

